struct CoordinateModel {
    let shoese: Int
    let bottom: Int
    let mask: Int
    let sunglasses: Int
    let umbrella: Int
    let top: Int
    
    init(shoese: Int, bottom: Int, mask: Int, sunglasses: Int, umbrella: Int, top: Int) {
        self.shoese = shoese
        self.bottom = bottom
        self.mask = mask
        self.sunglasses = sunglasses
        self.umbrella = umbrella
        self.top = top
    }
}
