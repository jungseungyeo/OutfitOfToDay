import UIKit


extension UIColor {
	
	@nonobjc class var skyBlue: UIColor {
		return UIColor(red: 92.0 / 255.0, green: 173.0 / 255.0, blue: 1.0, alpha: 1.0)
	}
	
	@nonobjc class var rosyPink: UIColor {
		return UIColor(red: 238.0 / 255.0, green: 88.0 / 255.0, blue: 94.0 / 255.0, alpha: 1.0)
	}
	
	@nonobjc class var gunmetal: UIColor {
		return UIColor(red: 69.0 / 255.0, green: 81.0 / 255.0, blue: 90.0 / 255.0, alpha: 1.0)
	}
	
}
